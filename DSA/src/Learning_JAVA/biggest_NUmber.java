package Learning_JAVA;

import java.util.Scanner;

public class biggest_NUmber {

	public static void main(String[] args) {
		Scanner sc =new Scanner(System.in);
		int m =sc.nextInt();
		int n =sc.nextInt();
		if(m>n) {
			System.out.println(m+" is bigger");
		}else {
			System.out.println(n+" is bigger");
		}

	}

}
